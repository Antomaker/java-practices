public class ConceptosPOO {

    public String obtenerDefinicion() {
       return """
        La Programación Orientada a Objetos (POO) es un paradigma de programación basado en objetos
        que encapsulan datos y comportamientos. Sus pilares fundamentales son: Abstracción, Encapsulamiento,
        Herencia y Polimorfismo.
        
        Abstracción: Simplificar y modelar entidades del mundo real.
        
        Encapsulamiento: Ocultar detalles internos y proteger datos.
        
        Herencia: Permitir que una clase herede atributos y métodos de otra.
        
        Polimorfismo: Permitir que objetos de diferentes clases respondan al mismo mensaje de manera específica.
        """;
    }
}
